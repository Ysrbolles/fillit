#include "libft.h"

int		ft_wordcount(char *str)
{
	int i;
	int k;

	i = 0;
	k = 1;
	while (str[i])
	{
		if (str[i] == ' ' || str[i] == '\t' || str[i] == '\n')
		{
			k++;
			if (str[i - 1] == ' ' || str[i - 1] == '\t' || str[i - 1] == '\n')
			{
				if (str[i + 1] == '\0')
					k--;
				k--;
			}
		}
		i++;
	}
	if (str[0] == ' ' || str[0] == '\t' || str[0] == '\n')
		k--;
	return (k);
}
