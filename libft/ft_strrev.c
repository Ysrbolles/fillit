#include "libft.h"

char	*ft_strrev(char *str)
{
	int		c;
	int		i;
	char	s;

	c = 0;
	while (str[c] != '\0')
		c++;
	c = c - 1;
	i = 0;
	while (i < ((c + 1) / 2))
	{
		s = str[i];
		str[i] = str[c - i];
		str[c - i] = s;
		i++;
	}
	return (str);
}
