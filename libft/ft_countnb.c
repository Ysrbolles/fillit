#include "libft.h"

int	ft_countnb(int n)
{
	unsigned int i;
	unsigned int nb;

	i = 0;
	if (n < 0)
	{
		nb = n * -1;
		i++;
	}
	else
		nb = n;
	while (nb > 0)
	{
		i++;
		nb /= 10;
	}
	return (i);
}
